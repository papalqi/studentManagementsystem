package com.studentgrade.backinfo;

import java.math.BigDecimal;
import java.util.ArrayList;
import java.util.List;

/**
 * <p>Title: StudentListInfo<／p>
 * <p>Description: <／p>
 * <p>Company: Oracle Group4<／p> 
 * @author XuChongtian
 * @date 2018年3月14日
 */
public class StudentListInfo {
	private int status;
	private List<StudentListItem> info = new ArrayList<StudentListItem>();
	
	
	public int getStatus() {
		return status;
	}


	public void setStatus(int status) {
		this.status = status;
	}


	public List<StudentListItem> getInfo() {
		return info;
	}


	public void setInfo(List<StudentListItem> info) {
		this.info = info;
	}


	public static class StudentListItem {
		
		private String stuname;
		private BigDecimal stuid;
		private String stuclass;
		private String stucollege;
		private String stumajor;
		
		public String getStuname() {
			return stuname;
		}
		public void setStuname(String stuname) {
			this.stuname = stuname;
		}
		public BigDecimal getStuid() {
			return stuid;
		}
		public void setStuid(BigDecimal stuid) {
			this.stuid = stuid;
		}
		public String getStuclass() {
			return stuclass;
		}
		public void setStuclass(String stuclass) {
			this.stuclass = stuclass;
		}
		public String getStucollege() {
			return stucollege;
		}
		public void setStucollege(String stucollege) {
			this.stucollege = stucollege;
		}
		public String getStumajor() {
			return stumajor;
		}
		public void setStumajor(String stumajor) {
			this.stumajor = stumajor;
		}
	}
}
