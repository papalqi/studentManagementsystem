public class InsertStudentInfo {

	private String sName;
	private String gender;
	private BigDecimal age;
	private BigDecimal studentID;
	private BigDecimal classID;
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public BigDecimal getAge() {
		return age;
	}
	public void setAge(BigDecimal age) {
		this.age = age;
	}
	public BigDecimal getStudentID() {
		return studentID;
	}
	public void setStudentID(BigDecimal studentID) {
		this.studentID = studentID;
	}
	public BigDecimal getClassID() {
		return classID;
	}
	public void setClassID(BigDecimal classID) {
		this.classID = classID;
	}
	
	
}
